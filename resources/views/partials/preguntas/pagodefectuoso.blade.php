<h3>¿Puedo obtener dinero si el dispositivo electrónico no funciona?</h3>
<p style="padding: 2%; margin-top: 5%; margin-bottom: 5%">Sí. En el caso que tu dispositivo electrónico no funcione te pagamos el 17% del valor de tu dispositivo electrónico.
Dispositivos electrónicos que no funcionan:</br></br>
El dispositivo no puede encenderse y/o apagarse</br>
La pantalla LCD/pantalla táctil está defectuosa o no funciona</br>
La pantalla tiene manchas, pixeles dañados o muertos</br>
El panel frontal de vidrio presenta grietas</br>
Faltan algunas de sus partes constitutivas (batería y/o tapa trasera)</br>
La  tecla cursor no esta presente (en los dispositivos Blackberry).</p>